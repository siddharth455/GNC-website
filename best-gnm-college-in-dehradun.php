  // ─── SCROLL PROGRESS ───────────────────────────────────────────
  const progressBar = document.getElementById('progress-bar');
  const backTop = document.getElementById('back-top');
  const nav = document.getElementById('gnm-nav');

  window.addEventListener('scroll', () => {
    const scrolled = window.scrollY;
    const max = document.body.scrollHeight - window.innerHeight;
    progressBar.style.width = (scrolled / max * 100) + '%';

    backTop.classList.toggle('show', scrolled > 400);
  });

  backTop.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));

  // ─── SCROLL REVEAL ─────────────────────────────────────────────
  const reveals = document.querySelectorAll('.reveal, .reveal-left, .reveal-right, .reveal-scale');
  const io = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('visible');
      } else {
        // Re-animate on scroll up
        e.target.classList.remove('visible');
      }
    });
  }, { threshold: 0.12 });
  reveals.forEach(el => io.observe(el));

  // ─── ANIMATED COUNTERS ─────────────────────────────────────────
  function animateCounter(el, target, suffix = '') {
    let current = 0;
    const step = target / 60;
    const timer = setInterval(() => {
      current += step;
      if (current >= target) { current = target; clearInterval(timer); }
      el.textContent = Math.floor(current) + suffix;
    }, 25);
  }

  const statObserver = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        const bigs = e.target.querySelectorAll('.big');
        bigs.forEach(b => {
          const text = b.textContent;
          const num = parseFloat(text);
          const suffix = text.replace(/[0-9.]/g, '');
          if (!isNaN(num)) animateCounter(b, num, suffix);
        });
        statObserver.unobserve(e.target);
      }
    });
  }, { threshold: 0.3 });
  document.querySelector('.stats-grid') && statObserver.observe(document.querySelector('.stats-grid'));

  // ─── PARALLAX HERO BG ──────────────────────────────────────────
  const heroBg = document.querySelector('.hero-bg');
  window.addEventListener('scroll', () => {
    if (heroBg) heroBg.style.transform = `scale(1.05) translateY(${window.scrollY * 0.08}px)`;
  }, { passive: true });

  // ─── TESTIMONIAL SLIDER ────────────────────────────────────────
