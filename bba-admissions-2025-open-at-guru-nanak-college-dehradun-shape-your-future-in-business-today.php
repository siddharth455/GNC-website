<script>
(function(){
  function getHeaderHeight() {
    const sel = ['header', '.site-header', '#site-header', '.header', '.main-header', '.topbar'];
    for (let s of sel) {
      const el = document.querySelector(s);
      if (el) {
        const rect = el.getBoundingClientRect();
        return Math.ceil(rect.height) + 8;
      }
    }
    return 120; // fallback
  }

  document.querySelectorAll('#toc a.toc-link').forEach(function(link){
    link.addEventListener('click', function(e){
      e.preventDefault();
      const targetId = this.getAttribute('href').slice(1);
      const target = document.getElementById(targetId);
      if (!target) return;
      const headerH = getHeaderHeight();
      const top = target.getBoundingClientRect().top + window.scrollY - headerH;
      window.scrollTo({ top: Math.max(0, Math.floor(top)), behavior: 'smooth' });
      // accessibility
      target.tabIndex = -1;
      target.focus({ preventScroll: true });
      if (history.replaceState) history.replaceState(null, '', '#' + targetId);
    });
  });

  // highlight active TOC entry while scrolling
  const tocLinks = Array.from(document.querySelectorAll('#toc a.toc-link'));
  const headings = tocLinks.map(l => document.getElementById(l.getAttribute('href').slice(1))).filter(Boolean);

  function computePositions(){ return headings.map(h => ({ id: h.id, top: h.getBoundingClientRect().top + window.scrollY })); }
  let positions = computePositions();
  window.addEventListener('resize', () => positions = computePositions());
  window.addEventListener('scroll', throttle(function(){
    const headerH = getHeaderHeight();
    const cursor = window.scrollY + headerH + 20;
    let active = -1;
    for (let i=0;i<positions.length;i++){ if (cursor >= positions[i].top) active = i; else break; }
    tocLinks.forEach((ln, idx) => ln.style.background = idx===active ? 'var(--toc-highlight)' : 'transparent');
  }, 120));

  function throttle(fn, wait) { let last = 0; return function() { const now = Date.now(); if (now - last >= wait) { last = now; fn(); } }; }
})();
</script>

