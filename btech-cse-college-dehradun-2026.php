<tr><td>Duration</td><td>4 years</td><td>3 years</td></tr>
<tr><td>Eligibility</td><td>12th PCM with Maths, min 45%</td><td>12th any stream, Maths preferred</td></tr>
<tr><td>Depth of Technical Training</td><td>Deep — algorithms, hardware, OS, networks</td><td>Moderate — programming, web, software</td></tr>
<tr><td>Avg Starting Salary</td><td>₹4–8 LPA</td><td>₹3.5–6 LPA</td></tr>
