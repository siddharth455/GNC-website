(function () {
  const content = document.getElementById('post-content');
  const toc = document.getElementById('toc');
  if (!content || !toc) return;

  const headings = content.querySelectorAll('h1, h2, h3');
  if (!headings.length) return;

  toc.style.display = 'block';
  const title = document.createElement('h4'); title.textContent = 'Table of Contents';
  toc.appendChild(title);
  const container = document.createElement('div');
  container.className = 'toc-container';
  toc.appendChild(container);

  function getHeaderHeight() {
    const sel = ['header', '.site-header', '#site-header', '.header', '.main-header', '.topbar'];
    for (let s of sel) {
      const el = document.querySelector(s);
      if (el) {
        const rect = el.getBoundingClientRect();
        return Math.ceil(rect.height) + 8;
      }
    }
    return 140;
  }

  headings.forEach((h, i) => {
    if (!h.id) {
      const raw = h.textContent.trim().toLowerCase().replace(/[^a-z0-9\s\-]+/g, '');
      let slug = raw.replace(/\s+/g, '-').slice(0, 120) || ('heading-' + i);
      let uniq = slug; let c = 1;
      while (document.getElementById(uniq)) { uniq = slug + '-' + c++; }
      h.id = uniq;
    }
    const link = document.createElement('a');
    link.href = '#' + h.id;
    link.className = 'toc-link';
    link.textContent = h.textContent;
    link.addEventListener('click', function (e) {
      e.preventDefault();
      const headerH = getHeaderHeight();
      const target = document.getElementById(h.id);
      if (!target) return;
      const top = target.getBoundingClientRect().top + window.scrollY - headerH;
      window.scrollTo({ top: Math.max(0, Math.floor(top)), behavior: 'smooth' });
      target.tabIndex = -1;
      target.focus({ preventScroll: true });
      if (history.replaceState) history.replaceState(null, '', '#' + h.id);
    });
    container.appendChild(link);
  });

  const tocLinks = Array.from(container.querySelectorAll('.toc-link'));
  let positions = computePositions();
  window.addEventListener('resize', () => { positions = computePositions(); });
  window.addEventListener('scroll', throttle(onScroll, 120));

  function computePositions() {
    return Array.from(headings).map(h => ({ id: h.id, top: h.getBoundingClientRect().top + window.scrollY }));
  }

  function onScroll() {
    const headerH = getHeaderHeight();
    const cursor = window.scrollY + headerH + 20;
    let active = -1;
    for (let i = 0; i < positions.length; i++) {
      if (cursor >= positions[i].top) active = i;
      else break;
    }
    tocLinks.forEach((ln, idx) => ln.style.background = (idx === active ? getComputedStyle(document.documentElement).getPropertyValue('--toc-highlight') || 'rgba(0,102,204,0.06)' : 'transparent'));
  }

  function throttle(fn, wait) {
    let last = 0;
    return function () {
      const now = Date.now();
      if (now - last >= wait) {
        last = now;
        fn();
      }
    };
  }

  positions = computePositions();
  onScroll();
})();
