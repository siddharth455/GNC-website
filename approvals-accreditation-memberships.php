    // JavaScript code to add fade-up animation when the section enters the viewport
    document.addEventListener("DOMContentLoaded", function() {
        const sections = document.querySelectorAll('.quality'); // Select all sections with the 'quality' class

        function checkVisibility() {
            const windowHeight = window.innerHeight;

            sections.forEach(section => {
                const sectionTop = section.getBoundingClientRect().top;

                if (sectionTop < windowHeight * 0.8) { // When the section is 80% visible
                    section.classList.add('fadeUp');
                }
            });
        }

        // Check visibility on scroll and resize
        window.addEventListener('scroll', checkVisibility);
        window.addEventListener('resize', checkVisibility);

        // Initial check
        checkVisibility();
    });
