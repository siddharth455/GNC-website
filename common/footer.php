    const header = document.querySelector("header");
    const toggleClass = "sticky";

    window.addEventListener("scroll", () => {
        const currentScroll = window.pageYOffset;
        if (currentScroll > 150) {
            header.classList.add(toggleClass);
        } else {
            header.classList.remove(toggleClass);
        }
    });

    var scrolling;
    var position = 0;
    var delta = 5;
    var hh = $('header').outerHeight();
    var dh = $(document).height();
    var wh = $(window).height();


    $(window).scroll(function(event) {
        scrolling = true;
    });

    setInterval(function() {
        if (scrolling) {
            scrollFunction();
            scrolling = false;
        }
    }, 250);

    function scrollFunction() {
        var st = $(this).scrollTop();

        if (Math.abs(position - st) <= delta)
            return;

        if (st > position && st > hh) {
            $('header').removeClass('scroll-down').addClass('scroll-up');
            $('.head-notifications h6').slideUp(200);
            $('.head-notifications .slick-slide').slideUp(200);
            $(".footer-fixed-bar").slideDown(200);
        } else {
            if (st + wh < dh) {
                $('header').removeClass('scroll-up').addClass('scroll-down');
                $('.head-notifications h6').slideDown(200);
                $('.head-notifications .slick-slide').slideDown(200);
                $(".footer-fixed-bar").slideUp(200);
            }
        }

        if (st > position && st > hh && $(window).width() < 768) {
            $('.mob-lower').slideUp(200);
        } else {
            if (st + wh < dh && $(window).width() < 768) {
                $('.mob-lower').slideDown(200);
            }
        }

        position = st;
    }
