<script>
    document.addEventListener("DOMContentLoaded", function() {
        const tabs = document.querySelectorAll(".step-item");
        const slides = document.querySelectorAll(".step-content");
        let currentIndex = 0;
        let autoPlayTimer;

        function showSlide(index) {
            // Remove active classes
            tabs.forEach(tab => tab.classList.remove("active"));
            slides.forEach(slide => slide.classList.remove("active"));

            // Set new active state
            tabs[index].classList.add("active");
            slides[index].classList.add("active");
            currentIndex = index;
        }

        function nextSlide() {
            let next = (currentIndex + 1) % tabs.length;
            showSlide(next);
        }

        // Start Auto-Switching (5 Seconds)
        function startAutoPlay() {
            autoPlayTimer = setInterval(nextSlide, 5000);
        }

        function resetAutoPlay() {
            clearInterval(autoPlayTimer);
            startAutoPlay();
        }

        // Manual Click Handler
        tabs.forEach((tab, index) => {
            tab.addEventListener("click", () => {
                showSlide(index);
                resetAutoPlay(); // Reset timer so it doesn't switch immediately after clicking
            });
        });

        // Initialize
        startAutoPlay();
    });
</script>

