        (function() {
            const activeImg = document.getElementById('activeImage');
            const activeCap = document.getElementById('activeCaption');
            const thumbItems = document.querySelectorAll('.thumb-item');
            const nextBtn = document.getElementById('goNext');
            const prevBtn = document.getElementById('goPrev');
            let currentIndex = 0;

            function updateGallery(index) {
                currentIndex = index;

                // Remove active classes
                thumbItems.forEach(item => item.classList.remove('active'));

                // Set new active
                const target = thumbItems[currentIndex];
                target.classList.add('active');

                // Update Display
                activeImg.src = target.querySelector('img').src;
                activeCap.textContent = target.getAttribute('data-caption');

                // Scroll thumbnail into view
                target.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'center' });
            }

            // Thumbnail Clicks
            thumbItems.forEach((item, index) => {
                item.addEventListener('click', () => updateGallery(index));
            });

            // Button Clicks
            nextBtn.addEventListener('click', () => {
                let next = (currentIndex + 1) % thumbItems.length;
                updateGallery(next);
            });

            prevBtn.addEventListener('click', () => {
                let prev = (currentIndex - 1 + thumbItems.length) % thumbItems.length;
                updateGallery(prev);
            });

            // Initial load
            updateGallery(0);
        })();
