<h2 id="eligibility">Eligibility Criteria — B.Sc Nursing vs GNM</h2>

<h3>Eligibility for B.Sc Nursing</h3>
<ul>
<li>Passed Class 12 (10+2) with Physics, Chemistry and Biology as compulsory subjects</li>
<li>Minimum 45% aggregate marks in PCB (some states require 50%)</li>
<li>Minimum age 17 years at the time of admission</li>
<li>English as a subject in Class 12 is preferred by most colleges</li>
<li>Medical fitness certificate required</li>
<li>No NEET required — direct merit-based admission</li>
</ul>

<h3>Eligibility for GNM</h3>
<ul>
<li>Passed Class 12 (10+2) with Physics, Chemistry and Biology</li>
<li>Minimum 40% aggregate marks in PCB</li>
<li>Minimum age 17 years at the time of admission</li>
<li>Arts stream students with Biology are eligible at some GNM colleges</li>
<li>No entrance exam — merit-based on Class 12 marks</li>
<li>Registered Auxiliary Nurse Midwife (ANM) with 1 year experience may also apply</li>
</ul>


