<h2 id="what-is-bsc-nursing">What is B.Sc Nursing?</h2>

<p>Bachelor of Science in Nursing (B.Sc Nursing) is a 4-year full-time undergraduate degree programme approved and regulated by the Indian Nursing Council (INC). It is the most comprehensive nursing qualification in India, combining classroom theory, laboratory training and compulsory clinical postings across major hospital departments.</p>

<p>B.Sc Nursing graduates are registered as professional nurses with the State Nursing Council after completing their degree — making them eligible for staff nurse positions in government and private hospitals, ICUs, operation theatres, paediatric wards, community health centres and internationally.</p>

<p>The programme is offered under university affiliation — in Uttarakhand, B.Sc Nursing colleges are affiliated to Hemwati Nandan Bahuguna Garhwal University (HNBGU) or Uttarakhand Medical Education University.</p>


<h2 id="what-is-gnm">What is GNM?</h2>

<p>General Nursing and Midwifery (GNM) is a 3-year diploma programme followed by a 6-month internship. It is also regulated by the Indian Nursing Council (INC) and is one of the oldest nursing qualifications in India. GNM prepares students for general nursing and midwifery practice and qualifies them for registered nurse roles in government and private healthcare settings.</p>

<p>GNM is a diploma, not a degree. While it qualifies you for nursing practice and most government posts, it does not carry the same academic standing as B.Sc Nursing for higher studies, international registration and senior management positions.</p>


