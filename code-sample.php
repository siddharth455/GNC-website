<script>
// Scroll Reveal
const revealEls = document.querySelectorAll('.reveal, .reveal-left, .reveal-right, .reveal-scale');
const observer = new IntersectionObserver((entries) => {
    entries.forEach(e => {
        if (e.isIntersecting) {
            e.target.classList.add('active');
            observer.unobserve(e.target);
        }
    });
}, { threshold: 0.12 });
revealEls.forEach(el => observer.observe(el));

// Counter Animation
function animateCounters() {
    document.querySelectorAll('.count').forEach(counter => {
        const target = parseInt(counter.getAttribute('data-target'));
        const suffix = counter.textContent.includes('%') ? '%' : '+';
        let current = 0;
        const step = target / 80;
        const timer = setInterval(() => {
            current += step;
            if (current >= target) {
                counter.textContent = target + suffix;
                clearInterval(timer);
            } else {
                counter.textContent = Math.floor(current) + suffix;
            }
        }, 20);
    });
}
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(e => {
        if (e.isIntersecting) { animateCounters(); statsObserver.disconnect(); }
    });
}, { threshold: 0.3 });
const statsEl = document.querySelector('.stats-strip');
if (statsEl) statsObserver.observe(statsEl);

// Semester Tabs
function showSem(id, btn) {
    document.querySelectorAll('.sem-content').forEach(c => c.classList.remove('active'));
    document.querySelectorAll('.sem-btn').forEach(b => b.classList.remove('active'));
    document.getElementById(id).classList.add('active');
    btn.classList.add('active');
}

