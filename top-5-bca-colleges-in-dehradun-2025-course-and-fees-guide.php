          <h2>3. Graphic Era University</h2>
          <p><strong>Location:</strong> Clement Town, Dehradun</p>
          <ul>
            <li><strong>Accreditation:</strong> NAAC A+, UGC-Recognized, AICTE Approved</li>
            <li><strong>Eligibility:</strong> 10+2 with at least 50% marks (preferably with Maths or CS)</li>
            <li><strong>Entrance Test:</strong> GEJET or merit-based</li>
            <li><strong>Annual Fees:</strong> ₹1.60 Lakhs</li>
          </ul>
          <p><strong>Placement Highlights:</strong></p>
          <ul>
            <li>Average Package: ₹5 LPA</li>
            <li>Top Recruiters: Infosys, TCS, Capgemini, Wipro</li>
          </ul>
          <p><strong>Why Choose:</strong> Graphic Era offers a modern BCA curriculum with specializations like Data Science and Cloud Computing. With excellent infrastructure, tech-focused faculty, and international tie-ups, it’s ideal for students targeting premium IT roles or global careers.</p>

          <h2>4. DIT University</h2>
          <p><strong>Location:</strong> Mussoorie Diversion Road, Dehradun</p>
          <ul>
            <li><strong>Accreditation:</strong> UGC Approved, NAAC Accredited</li>
            <li><strong>Eligibility:</strong> 10+2 with at least 50% marks (preferably with Mathematics)</li>
            <li><strong>Entrance Test:</strong> Merit-based + Personal Interview</li>
            <li><strong>Annual Fees:</strong> ₹1.25 Lakhs</li>
          </ul>
          <p><strong>Placement Highlights:</strong></p>
          <ul>
            <li>Average Package: ₹4 LPA</li>
            <li>Recruiters: TCS, Cognizant, Deloitte, HCL</li>
          </ul>
          <p><strong>Why Choose:</strong> DIT University’s BCA program integrates IT education with business knowledge, offering exposure to coding, data analytics, and soft skills. Its project-based approach and strong corporate network make it a tech-savvy institution.</p>

          <h2>5. UPES Dehradun</h2>
          <p><strong>Location:</strong> Bidholi & Kandoli Campuses, Dehradun</p>
          <ul>
            <li><strong>Accreditation:</strong> NAAC A, UGC recognized, AIU Member</li>
            <li><strong>Eligibility:</strong> 10+2 with minimum 50% marks (preferably with Computer Science or Mathematics)</li>
            <li><strong>Entrance Test:</strong> CUET / UPESEAT / SAT / Board Merit</li>
            <li><strong>Annual Fees:</strong> ₹2.70 Lakhs</li>
          </ul>
          <p><strong>Placement Highlights:</strong></p>
          <ul>
            <li>Highest Package: ₹7.8 LPA</li>
            <li>Average Package: ₹4.5 LPA</li>
