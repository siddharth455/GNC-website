          <p><strong>Location:</strong> Race Course Road, Dehradun (Prime Central Location)</p>
          <ul>
            <li><strong>Affiliation:</strong> Sri Dev Suman Uttarakhand University (SDSU)</li>
            <li><strong>Eligibility:</strong> 10+2 with Mathematics/Computer Science and minimum 45% marks</li>
            <li><strong>Entrance Test:</strong> No entrance test; direct merit-based admission</li>
            <li><strong>Annual Fees:</strong> ₹35,000</li>
          </ul>
          <p><strong>Placement Highlights:</strong></p>
          <ul>
            <li>Highest Package: ₹5 LPA</li>
            <li>Average Package: ₹3 LPA</li>
            <li>Recruiters: Startups, IT firms, edtech companies, software support teams</li>
          </ul>
          <p><strong>Why Choose:</strong> Guru Nanak College is gaining recognition as a leading BCA college in Dehradun. The program emphasizes hands-on learning, coding skills, and real-world projects. Its affordability, strong academic support, and central location make it a smart choice for aspiring IT professionals.</p>
