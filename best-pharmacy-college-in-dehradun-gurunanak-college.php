        e.preventDefault();
        const id = this.getAttribute('href').slice(1);
        const target = document.getElementById(id);
        if (!target) return;
        const headerH = getHeaderHeight();
        const top = target.getBoundingClientRect().top + window.scrollY - headerH;
        window.scrollTo({ top: Math.max(0, Math.floor(top)), behavior: 'smooth' });
        // accessibility
        target.tabIndex = -1;
        target.focus({ preventScroll: true });
        if (history.replaceState) history.replaceState(null, '', '#' + id);
      });
    });

    // Highlight TOC while scrolling
    const tocLinks = Array.from(document.querySelectorAll('#toc a.toc-link'));
    const headings = tocLinks.map(l => document.getElementById(l.getAttribute('href').slice(1))).filter(Boolean);

    function positions() { return headings.map(h => ({ id: h.id, top: h.getBoundingClientRect().top + window.scrollY })); }
    let pos = positions();

    window.addEventListener('resize', () => pos = positions());
    window.addEventListener('scroll', throttle(function(){
      const headerH = getHeaderHeight();
      const cursor = window.scrollY + headerH + 20;
      let active = -1;
      for (let i=0;i<pos.length;i++){ if (cursor >= pos[i].top) active = i; else break; }
      tocLinks.forEach((ln, idx) => ln.style.background = idx===active ? 'rgba(0,102,204,0.06)' : 'transparent');
    }, 120));

